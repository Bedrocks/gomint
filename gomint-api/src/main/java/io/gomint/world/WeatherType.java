public enum WeatherType {

	/**
	 * Specifies entirely clear sky and sunshine!
	 */
	CLEAR,

	/**
	 * Rain or (snow) will fall in the respective biomes
	 */
	DOWNFALL,

	/**
	 * A thunderstorm will take place
	 */
	THUNDERSTORM;

}
